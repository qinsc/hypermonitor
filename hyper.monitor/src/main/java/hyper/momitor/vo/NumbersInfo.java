package hyper.momitor.vo;

public class NumbersInfo {
	private int hosts;
	private int onlineHosts;
	private int hostGroups;
	private int hostTags;

	/**
	 * @return the hosts
	 */
	public int getHosts() {
		return hosts;
	}

	/**
	 * @param hosts
	 *            the hosts to set
	 */
	public void setHosts(int hosts) {
		this.hosts = hosts;
	}

	/**
	 * @return the onlineHosts
	 */
	public int getOnlineHosts() {
		return onlineHosts;
	}

	/**
	 * @param onlineHosts
	 *            the onlineHosts to set
	 */
	public void setOnlineHosts(int onlineHosts) {
		this.onlineHosts = onlineHosts;
	}

	/**
	 * @return the hostGroups
	 */
	public int getHostGroups() {
		return hostGroups;
	}

	/**
	 * @param hostGroups
	 *            the hostGroups to set
	 */
	public void setHostGroups(int hostGroups) {
		this.hostGroups = hostGroups;
	}

	/**
	 * @return the hostTags
	 */
	public int getHostTags() {
		return hostTags;
	}

	/**
	 * @param hostTags
	 *            the hostTags to set
	 */
	public void setHostTags(int hostTags) {
		this.hostTags = hostTags;
	}

}
